package com.breno.listapersonagens;

public class PersoInfo {
    public int imgPerso;
    public String nome, idade, ator, classe, temp;

    public PersoInfo(int imgPerso, String nome, String idade, String ator, String classe, String temp) {
        this.imgPerso = imgPerso;
        this.nome = nome;
        this.idade = idade;
        this.ator = ator;
        this.classe = classe;
        this.temp = temp;
    }
}
